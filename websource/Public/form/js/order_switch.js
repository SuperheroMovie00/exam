function dealOrderSwitchAlert(l){

    var node=$('#'+l.getAlertId());

    node.on('click','input[type="button"]',function(){
        $('#'+l.getFormId()).parent().show();
        l.close(2);
    });

    node.on('click','input[type="submit"]',function(){
        $('#'+l.getFormId()).parent().show();
        l.close(2);
    });

    node.on('click','a.close ',function(){
        $('#'+l.getFormId()).parent().show();
        l.close(2);
    });

}